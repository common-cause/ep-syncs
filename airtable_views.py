@dataclass
class Link:
    """Record-link fallback: resolve a column off a linked record."""
    via: List[str]        # candidate link columns (JSON id-array strings)
    target_key: str       # table_key of the link target within the same base
    target_col: str       # column to take from the target table
    alias: str            # stable join alias


@dataclass
class Col:
    name: str
    bq_type: str                              # STRING | INT64 | BOOL | TIMESTAMP
    sources: List[str] = field(default_factory=list)
    transform: Optional[str] = None           # 'norm_email' | 'norm_phone'
    regex_source: Optional[str] = None        # value column matched by pattern
    regex_capture: Optional[str] = None       # literal INT from pattern capture
    link: Optional[Link] = None               # fallback when no source present
    computed: Optional[str] = None            # special-cased in _branch_select


