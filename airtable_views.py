"""
Generator for the ep_2026_cleaned Airtable union views.

Renders CREATE OR REPLACE VIEW statements that UNION each registered base's
typed tables (ep_2026_raw.{prefix}__{table}) into one cross-state view per
entity: quiz_responses, incident_reports, checklist_submissions,
polling_places, shifted_volunteers.

Inputs, resolved live at render time:
  - the base registry (ep.airtable_sync_sources, enabled rows) -- provides
    state, base_type, prefix, and per-base canonical_overrides JSON;
  - ep_2026_raw.INFORMATION_SCHEMA.COLUMNS -- which columns each typed
    table actually landed with (and their types, so casts are exact);
  - the per-entity canonical contracts below (DEFAULT source-column
    candidates, auto-heuristics, link-resolution fallbacks).

Per canonical column, source resolution order:
  registry canonical_overrides -> default candidates -> regex heuristic ->
  record-link fallback -> NULL-pad (CAST(NULL AS T)).

canonical_overrides shape (JSON on the registry row):
  {"<entity>": {"<canonical_col>": "<sanitized_source_col>" | null}}
String forces the source column; null removes (NULL-pads) the column for
that base. Overrides bypass candidates/heuristics but keep transforms/casts.

Entities with no live base render as the full contract + WHERE FALSE, so
consumers can build against the schema before any base goes live.

Used two ways:
  - sync_airtable_bases.py calls render + apply at the end of each run
    (a base going live updates the unions the same morning);
  - apply_bq_views.py --render-generated rewrites the committed
    bq/ep_2026_cleaned/3x_*.sql snapshots for review/commit.
"""

