# -- Inputs -------------------------------------------------------------------


@dataclass
class RegistryRow:
    name: str
    state: str
    base_id: str
    base_type: str
    prefix: str
    overrides: Dict[str, Dict[str, Optional[str]]]


def load_registry(bq: BigQueryConnector) -> List[RegistryRow]:
    sql = f"""
        SELECT name, state, base_id, base_type, bq_table_prefix,
               TO_JSON_STRING(canonical_overrides) AS overrides_json
        FROM `{SOURCES_TABLE}`
        WHERE enabled = TRUE
        ORDER BY bq_table_prefix
    """
    rows: List[RegistryRow] = []
    for r in bq.query(sql):
        raw = r["overrides_json"]
        overrides = json.loads(raw) if raw and raw != "null" else {}
        rows.append(RegistryRow(
            name=r["name"], state=r["state"], base_id=r["base_id"],
            base_type=r["base_type"], prefix=r["bq_table_prefix"],
            overrides=overrides or {},
        ))
    return rows


def load_landed_columns(bq: BigQueryConnector) -> Dict[str, Dict[str, str]]:
    """{typed_table_name: {column: data_type}} for all of ep_2026_raw."""
    sql = f"""
        SELECT table_name, column_name, data_type
        FROM `{PROJECT}.{RAW_DATASET}.INFORMATION_SCHEMA.COLUMNS`
    """
    out: Dict[str, Dict[str, str]] = {}
    for r in bq.query(sql):
        out.setdefault(r["table_name"], {})[r["column_name"]] = r["data_type"]
    return out


# -- Rendering ----------------------------------------------------------------


def _cast(expr: str, landed_type: Optional[str], target: str) -> str:
    """Exact minimal cast from the landed BQ type to the target type."""
    if landed_type == target:
        return expr
    if target == "STRING":
        return f"CAST({expr} AS STRING)"
    if target == "INT64":
        if landed_type == "FLOAT64":
            return f"SAFE_CAST({expr} AS INT64)"
        return f"SAFE_CAST(SAFE_CAST({expr} AS FLOAT64) AS INT64)"
    if target == "BOOL":
        return f"SAFE_CAST(CAST({expr} AS STRING) AS BOOL)"
    if target == "TIMESTAMP":
        return f"SAFE_CAST(CAST({expr} AS STRING) AS TIMESTAMP)"
    return f"SAFE_CAST({expr} AS {target})"


def _wrap_transform(expr: str, transform: Optional[str]) -> str:
    if transform == "norm_email":
        return f"{NORM_EMAIL}({expr})"
    if transform == "norm_phone":
        return f"{NORM_PHONE}({expr})"
    return expr


def _resolve_col(
    col: Col,
    entity: Entity,
    row: RegistryRow,
    table_cols: Dict[str, str],
    landed: Dict[str, Dict[str, str]],
    joins: Dict[str, Tuple[str, str]],   # alias -> (link_col, target_table)
) -> str:
    """Return the SELECT expression for one canonical column in one branch."""
    null_expr = f"CAST(NULL AS {col.bq_type})"

    # 1. Registry override (string forces source; null forces NULL-pad).
    ov = row.overrides.get(entity.view, {})
    if col.name in ov:
        source = ov[col.name]
        if source is None:
            return null_expr
        if source not in table_cols:
            logger.warning(
                f"[{row.prefix}] override {entity.view}.{col.name} -> "
                f"'{source}' not in landed columns -- NULL-padding"
            )
            return null_expr
        expr = _cast(f"t.`{source}`", table_cols[source], "STRING"
                     if col.transform else col.bq_type)
        return _wrap_transform(expr, col.transform)

    # 2. Computed columns (special-cased per entity; handled by caller).
    if col.computed:
        return ""  # placeholder -- caller substitutes

    # 3. Regex capture -> literal from the matching column NAME.
    if col.regex_capture:
        pat = re.compile(col.regex_capture)
        for c in sorted(table_cols):
            m = pat.match(c)
            if m:
                return f"CAST({int(m.group(1))} AS INT64)"
        return null_expr

    # 4. Regex-matched value column.
    if col.regex_source:
        pat = re.compile(col.regex_source)
        for c in sorted(table_cols):
            if pat.match(c):
                return _cast(f"t.`{c}`", table_cols[c], col.bq_type)
        return null_expr

    # 5. Direct candidates, first present wins.
    for c in col.sources:
        if c in table_cols:
            expr = _cast(f"t.`{c}`", table_cols[c], "STRING"
                         if col.transform else col.bq_type)
            return _wrap_transform(expr, col.transform)

    # 6. Record-link fallback.
    if col.link:
        target_table = f"{row.prefix}__{col.link.target_key}"
        if target_table in landed and col.link.target_col in landed[target_table]:
            for via in col.link.via:
                if via in table_cols:
                    joins[col.link.alias] = (via, target_table)
                    t_type = landed[target_table][col.link.target_col]
                    expr = _cast(
                        f"{col.link.alias}.`{col.link.target_col}`",
                        t_type,
                        "STRING" if col.transform else col.bq_type,
                    )
                    return _wrap_transform(expr, col.transform)
        return null_expr

    return null_expr


def _branch_sql(
    entity: Entity,
    row: RegistryRow,
    table_name: str,
    landed: Dict[str, Dict[str, str]],
) -> str:
    table_cols = landed[table_name]
    joins: Dict[str, Tuple[str, str]] = {}

    exprs: Dict[str, str] = {}
    for col in entity.columns:
        exprs[col.name] = _resolve_col(col, entity, row, table_cols, landed, joins)

    # Computed columns.
    for col in entity.columns:
        if col.computed == "passed":
            score, score_max = exprs.get("score"), exprs.get("score_max")
            if score and score_max and "NULL" not in (score, score_max):
                exprs[col.name] = f"({score}) = ({score_max})"
            else:
                exprs[col.name] = "CAST(NULL AS BOOL)"
        elif col.computed == "in_ptv":
            email = exprs.get("email") or "CAST(NULL AS STRING)"
            exprs[col.name] = (
                f"EXISTS(SELECT 1 FROM `{USERS_CURRENT_VIEW}` u "
                f"WHERE u.state = '{row.state}' AND u.email = {email})"
            )

    lines = [
        f"  '{row.state}' AS state,",
        f"  '{row.prefix}' AS base_key,",
        f"  '{row.base_id}' AS base_id,",
        "  t._airtable_record_id AS record_id,",
        "  t._airtable_created_time AS created_at,",
        "  t._synced_at AS synced_at,",
    ]
    lines += [f"  {exprs[c.name]} AS {c.name}," for c in entity.columns]
    lines[-1] = lines[-1].rstrip(",")

    sql = "SELECT\n" + "\n".join(lines)
    sql += f"\nFROM `{PROJECT}.{RAW_DATASET}.{table_name}` t"
    for alias in sorted(joins):
        link_col, target_table = joins[alias]
        sql += (
            f"\nLEFT JOIN `{PROJECT}.{RAW_DATASET}.{target_table}` {alias}"
            f"\n  ON {alias}._airtable_record_id = JSON_VALUE(t.`{link_col}`, '$[0]')"
        )
    return sql


def _bootstrap_sql(entity: Entity) -> str:
    """Full-contract empty branch so the view exists before any base does."""
    lines = [
        "  CAST(NULL AS STRING) AS state,",
        "  CAST(NULL AS STRING) AS base_key,",
        "  CAST(NULL AS STRING) AS base_id,",
        "  CAST(NULL AS STRING) AS record_id,",
        "  CAST(NULL AS TIMESTAMP) AS created_at,",
        "  CAST(NULL AS TIMESTAMP) AS synced_at,",
    ]
    lines += [f"  CAST(NULL AS {c.bq_type}) AS {c.name}," for c in entity.columns]
    lines[-1] = lines[-1].rstrip(",")
    return "SELECT\n" + "\n".join(lines) + "\nWHERE FALSE"


def render_entity_views(bq: BigQueryConnector) -> Dict[str, str]:
    """Render all union views. Returns {snapshot_filename: full_sql}."""
    registry = load_registry(bq)
    landed = load_landed_columns(bq)

    out: Dict[str, str] = {}
    for entity in ENTITIES:
        branches: List[str] = []
        for row in registry:
            if row.base_type not in entity.base_types:
                continue
            for key in entity.table_keys:
                table_name = f"{row.prefix}__{key}"
                if table_name in landed:
                    branches.append(_branch_sql(entity, row, table_name, landed))
        body = "\nUNION ALL\n".join(branches) if branches else _bootstrap_sql(entity)
        description = entity.description.replace('"', "'")
        sql = (
            f"{GENERATED_HEADER}\n"
            f"CREATE OR REPLACE VIEW `{PROJECT}.{CLEAN_DATASET}.{entity.view}`\n"
            f'OPTIONS(description="{description}")\n'
            f"AS\n{body};\n"
        )
        out[ENTITY_FILES[entity.view]] = sql
        logger.info(
            f"[views] {entity.view}: {len(branches)} branch(es)"
            + ("" if branches else " (bootstrap WHERE FALSE)")
        )
    return out


def write_snapshots(views: Dict[str, str]) -> None:
    for fname, sql in views.items():
        (SQL_DIR / fname).write_text(sql, encoding="utf-8", newline="\n")
        logger.info(f"[views] wrote {fname}")


def apply_views(bq: BigQueryConnector, views: Dict[str, str]) -> None:
    for fname in sorted(views):
        bq.query(views[fname])
        logger.info(f"[views] applied {fname}")


def render_write_apply(bq: BigQueryConnector) -> None:
    """The post-sync hook: regenerate, snapshot to disk, and deploy."""
    views = render_entity_views(bq)
    write_snapshots(views)
    apply_views(bq, views)
